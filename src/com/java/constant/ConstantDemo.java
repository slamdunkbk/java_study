package com.java.constant;

public class ConstantDemo {

	public static void main(String[] args) {
		float a = 2.2f;
		double b = 2.2;
		System.out.println(a);
		System.out.println(b);
		
		byte c = 127;
		short d = 32767;
		int e = 2147483647;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
	}

}
