package com.java.compare;

public class Equal {

	public static void main(String[] args) {
		int a=1;
		System.out.println(a==2);
		System.out.println(a==1);
		System.out.println("one"=="two");
		System.out.println("one"=="one");
		
		System.out.println(a!=2);
		System.out.println(a!=1);
		System.out.println("one"!="two");
		System.out.println("one"!="one");
	}

}
